    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        ChiTietSanPham ctsp = new ChiTietSanPham();
        ctsp.setNamBH(Integer.valueOf(txtNamBH.getText()));
        ctsp.setGiaBan(BigDecimal.valueOf(Double.valueOf(txtGiaBan.getText())));
        ctsp.setGiaNhap(BigDecimal.valueOf(Double.valueOf(txtGiaNhap.getText())));
        ctsp.setSoLuongTon(Integer.valueOf(txtSoLuongTon.getText()));
        ctsp.setMoTa(txtAreaMoTa.getText());
        SanPham sp = sps.getByMaSP(String.valueOf(cbbSanPham.getSelectedItem()));
        ctsp.setSanPham(sp);
        MauSac ms = mss.getByMaMauSac(String.valueOf(cbbMauSac.getSelectedItem()));
        ctsp.setMauSac(ms);
        DongSanPham dsp = dsps.getByMaDSP(String.valueOf(cbbDongSP.getSelectedItem()));
        ctsp.setDongSanPham(dsp);
        NhaSanXuat nsx = nsxs.getByMaNSX(String.valueOf(cbbNSX.getSelectedItem()));
        ctsp.setNhaSanXuat(nsx);
        JOptionPane.showMessageDialog(this, String.valueOf(ctsps.saveCTSP(ctsp)));
        listCTSP = ctsps.getListCTSP();
        showDataTableCTSP(listCTSP);
    }//GEN-LAST:event_btnAddActionPerformed

