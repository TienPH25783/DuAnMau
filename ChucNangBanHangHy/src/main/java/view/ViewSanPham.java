    private void showDataTableCTSP(List<ChiTietSanPham> lists) {
        STT = 1;
        dtm.setRowCount(0);
        for (ChiTietSanPham ctsp : lists) {
            dtm.addRow(new Object[]{STT++, ctsp.getSanPham().getMaSP(), ctsp.getSanPham().getTenSP(),
                ctsp.getNhaSanXuat().getTenNSX(), ctsp.getMauSac().getTenMauSac(), ctsp.getDongSanPham().getTenDongSP(),
                ctsp.getNamBH(), ctsp.getMoTa(), ctsp.getSoLuongTon(), ctsp.getGiaNhap(), ctsp.getGiaBan()});
        }
    }

    private void loadCombobox() {

        for (SanPham sp : listSP) {
            comboSP.addElement(sp.getMaSP());
        }

        for (DongSanPham dsp : listDSP) {
            comboDSP.addElement(dsp.getMaDongSP());
        }

        for (NhaSanXuat nsx : listNSX) {
            comboNSX.addElement(nsx.getMaNSX());
        }

        for (MauSac ms : listMS) {
            comboMS.addElement(ms.getMaMauSac());
        }

    }

    private void fillData(int index) {
        ChiTietSanPham ctsp = listCTSP.get(index);
        cbbSanPham.setSelectedItem(ctsp.getSanPham().getMaSP());
        cbbMauSac.setSelectedItem(ctsp.getMauSac().getMaMauSac());
        cbbDongSP.setSelectedItem(ctsp.getDongSanPham().getMaDongSP());
        cbbNSX.setSelectedItem(ctsp.getNhaSanXuat().getMaNSX());
        txtNamBH.setText(String.valueOf(ctsp.getNamBH()));
        txtAreaMoTa.setText(ctsp.getMoTa());
        txtGiaBan.setText(String.valueOf(ctsp.getGiaBan()));
        txtGiaNhap.setText(String.valueOf(ctsp.getGiaNhap()));
        txtSoLuongTon.setText(String.valueOf(ctsp.getSoLuongTon()));
    }

    private void clearForm() {
        tbSanPham.clearSelection();
        txtAreaMoTa.setText("");
        txtGiaBan.setText("");
        txtGiaNhap.setText("");
        txtNamBH.setText("");
        txtSoLuongTon.setText("");
        cbbDongSP.setSelectedIndex(0);
        cbbMauSac.setSelectedIndex(0);
        cbbNSX.setSelectedIndex(0);
        cbbSanPham.setSelectedIndex(0);
    }

